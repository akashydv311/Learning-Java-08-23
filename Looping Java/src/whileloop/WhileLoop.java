package whileloop;

public class WhileLoop {
    
    public static void main(String[] args) {

        int num = 1;

            // 101 <= 100  false
        while( num <= 100 ){

            System.out.print(num +" " );
            num++;          // num = 101

        }
        System.out.println();
        System.out.println("Thank You!");

    }
}
